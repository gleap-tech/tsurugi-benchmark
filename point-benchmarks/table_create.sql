DROP TABLE IF EXISTS H_TRANSACTION;
DROP TABLE IF EXISTS S_ACCOUNT;
DROP TABLE IF EXISTS M_STORE;
DROP TABLE IF EXISTS M_REGION;

CREATE TABLE S_ACCOUNT (
    CARD_ID BIGINT PRIMARY KEY,
    BALANCE BIGINT NOT NULL,
    REGION_ID INT NOT NULL,
    LAST_UPDATE TIMESTAMP NOT NULL
);

CREATE TABLE H_TRANSACTION (
    PAYMENT_TIME TIMESTAMP NOT NULL,
    CARD_ID BIGINT NOT NULL,
    STORE_ID INT NOT NULL,
    AMOUNT BIGINT NOT NULL,
    BALANCE BIGINT NOT NULL,
    PRIMARY KEY (PAYMENT_TIME, CARD_ID, STORE_ID)
);

CREATE TABLE M_STORE (
    STORE_ID INT PRIMARY KEY,
    STORE_ZIPCODE VARCHAR(7) NOT NULL,
    STORE_ADDRESS VARCHAR(256) NOT NULL,
    STORE_NAME VARCHAR(256) NOT NULL,
    REGION_ID INT NOT NULL,
    S_LATITUDE DOUBLE PRECISION NOT NULL,
    S_LONGITUDE DOUBLE PRECISION NOT NULL
);

CREATE TABLE M_REGION (
    REGION_ID INT PRIMARY KEY,
    REGION_NAME VARCHAR(256) NOT NULL
);

CREATE INDEX IDX_S_ACCOUNT_REGION_ID ON S_ACCOUNT (REGION_ID);
CREATE INDEX IDX_M_STORE_REGION_ID ON M_STORE (REGION_ID);
CREATE INDEX IDX_H_TRANSACTION_CARD_ID ON H_TRANSACTION (CARD_ID);
CREATE INDEX IDX_H_TRANSACTION_PAYMENT_TIME ON H_TRANSACTION (PAYMENT_TIME);
CREATE INDEX IDX_H_TRANSACTION_AMOUNT_STORE_TIME
    ON H_TRANSACTION (PAYMENT_TIME, AMOUNT, STORE_ID);
